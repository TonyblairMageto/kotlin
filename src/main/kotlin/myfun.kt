fun main(){
    addition(34,12)
    subtraction(57,34)
    multiplication(3,2)
    division(24,4)
}

//adding numbers

fun addition(number1:Int, number2:Int){
    println(number1+number2)
}

//subtraction of numbers
fun subtraction(num1:Int, num2:Int){
    println(num1-num2)
}

//multiplying numbers
fun multiplication(value1:Int, value2:Int){
    println(value1*value2)
}

//dividing numbers
fun division(first:Int, sec:Int){
    println(first/sec)
}